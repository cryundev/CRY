#include "CRLog.h"
#include <comdef.h>


CRLog GLog;


//---------------------------------------------------------------------------------------------------------------------
/// Add log.
//---------------------------------------------------------------------------------------------------------------------
void CRLog::AddLog( const CRString& Log )
{
	Logs.push_back( Log );
}

//---------------------------------------------------------------------------------------------------------------------
/// Add error log.
//---------------------------------------------------------------------------------------------------------------------
void CRLog::AddErrorLog( HRESULT HR )
{
	_com_error comError( HR );

	ErrorLogs.emplace_back( comError.ErrorMessage() );
}
