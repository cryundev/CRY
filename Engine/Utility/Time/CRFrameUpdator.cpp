#include "CRFrameUpdator.h"


//---------------------------------------------------------------------------------------------------------------------
/// Initialize.
//---------------------------------------------------------------------------------------------------------------------
void CRFrameUpdator::Initialize( int InFPS )
{
    FPS = InFPS;
}

//---------------------------------------------------------------------------------------------------------------------
/// Update.
//---------------------------------------------------------------------------------------------------------------------
bool CRFrameUpdator::Update()
{
    float deltaSeconds = Time.Finish();

    bool bElapsed = deltaSeconds > ( 1.0f / (float)( FPS ) );

    if ( bElapsed )
    {
        Time.Start();
    }

    return bElapsed;
}
