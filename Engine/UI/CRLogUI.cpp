#include "CRLogUI.h"
#include "Core/ImGUI/imgui.h"
#include "Utility/Log/CRLog.h"


//---------------------------------------------------------------------------------------------------------------------
/// Draw.
//---------------------------------------------------------------------------------------------------------------------
void CRLogUI::Draw()
{
    for ( const auto& log : GLog.GetLogs() )
    {
        ImGui::Text( log.c_str() );
    }
}
