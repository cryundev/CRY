#include "CRUIManager.h"
#include "CREditorUI.h"
#include "ICRUI.h"


//---------------------------------------------------------------------------------------------------------------------
/// Destructor.
//---------------------------------------------------------------------------------------------------------------------
CRUIManager::~CRUIManager()
{
    Clear();
}

//---------------------------------------------------------------------------------------------------------------------
/// Initialize.
//---------------------------------------------------------------------------------------------------------------------
void CRUIManager::Initialize()
{
    CREditorUI* editorUi = new CREditorUI();
    if ( editorUi )
    {
        editorUi->Initialize();
        UIs.push_back( editorUi );
    }
}

//---------------------------------------------------------------------------------------------------------------------
/// Clear.
//---------------------------------------------------------------------------------------------------------------------
void CRUIManager::Clear()
{
    for ( auto& ui : UIs )
    {
        delete ui;
    }
}

//---------------------------------------------------------------------------------------------------------------------
/// Draw.
//---------------------------------------------------------------------------------------------------------------------
void CRUIManager::Draw() const
{
    for ( const auto& ui : UIs )
    {
        ui->Draw();
    }
}
