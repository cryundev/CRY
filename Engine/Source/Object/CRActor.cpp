#include "CRActor.h"


//---------------------------------------------------------------------------------------------------------------------
/// Destory
//---------------------------------------------------------------------------------------------------------------------
void CRActor::Destroy()
{
    CRObject::Destroy();
}

//---------------------------------------------------------------------------------------------------------------------
/// Initialize components.
//---------------------------------------------------------------------------------------------------------------------
void CRActor::InitializeComponents()
{
    if ( !CRIdentity::IsValid( ObjectId ) ) return;

    AddComponent< CRTransformComponent >();
}