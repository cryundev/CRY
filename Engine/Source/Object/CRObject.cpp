#include "CRObject.h"


//---------------------------------------------------------------------------------------------------------------------
/// Constructor
//---------------------------------------------------------------------------------------------------------------------
CRObject::CRObject()
{
}

//---------------------------------------------------------------------------------------------------------------------
/// Destroy
//---------------------------------------------------------------------------------------------------------------------
void CRObject::Destroy()
{
    CRIdentity::CRIDGenerator< CRObjectId >::Get().Remove( ObjectId );
    ObjectId = (CRObjectId)( CRIdentity::IdMask );
}
