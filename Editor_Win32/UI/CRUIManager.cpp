#include "CRUIManager.h"
#include "ICRUI.h"


//---------------------------------------------------------------------------------------------------------------------
/// Destructor.
//---------------------------------------------------------------------------------------------------------------------
CRUIManager::~CRUIManager()
{
    Clear();
}

//---------------------------------------------------------------------------------------------------------------------
/// Add UI.
//---------------------------------------------------------------------------------------------------------------------
void CRUIManager::AddUI( ICRUI* InUI )
{
    if ( !InUI ) return;

    InUI->Initialize();
    
    UIs.push_back( InUI );
}

//---------------------------------------------------------------------------------------------------------------------
/// Clear.
//---------------------------------------------------------------------------------------------------------------------
void CRUIManager::Clear()
{
    for ( auto& ui : UIs )
    {
        delete ui;
    }
}

//---------------------------------------------------------------------------------------------------------------------
/// Draw.
//---------------------------------------------------------------------------------------------------------------------
void CRUIManager::Draw() const
{
    for ( const auto& ui : UIs )
    {
        ui->Draw();
    }
}
